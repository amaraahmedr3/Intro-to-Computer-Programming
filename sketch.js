function setup()
{
    //create a large square canvas
    createCanvas(800, 800);
}

 

function draw()
{

 

    //Set the fill colour to white
    fill(255,255,255);
    
    //set the fill colour to red
    //fill(255, 0, 0);
    
        
    
    //set a thick stroke weight for the black lines
    strokeWeight(15);

 

    //draw the red rectangle 
    rect(100, 0, 800, 600);
    
    //draw the white rectangle
    rect (0,0,200,250);
    
    
    //draw the white rectangle
    rect (0,250,200,500);
    
    //draw the blue rectangle
    rect(0,600,200,600);
    
    //draw the small blue rectangle
    rect(600,600,700,700);
    
    //draw the small yellow rectangle
    rect(600,700,700,750);
    
    
   
   

}